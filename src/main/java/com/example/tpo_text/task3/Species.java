package com.example.tpo_text.task3;

class Species {
    private String name;
    private boolean furry;

    public Species(String name, boolean furry) {
        this.name = name;
        this.furry = furry;
    }

    public String getName() {
        return name;
    }

    public boolean isFurry() {
        return furry;
    }
}
